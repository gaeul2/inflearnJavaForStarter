package operator;

public class OperatorAdd1 {

    public static void main(String[] args) {
        int a = 0;
        System.out.println("a = " + a);
        a++;
        System.out.println("a = " + a);
        a++;
        System.out.println("a = " + a);
        ++a;
        System.out.println("a = " + a);
    }
}
